"""
Simular movimientos con el for
"""

import os
import time

espacios = " "

for i in range(20):


    time.sleep(0.3)
    os.system("clear")
    espacios += " "


    print()
    print("{}   0 ".format(espacios))
    print("{}  /|\ ".format(espacios))
    print("{}   |".format(espacios))
    print("{}   /\  ".format(espacios))


    time.sleep(0.3)
    os.system("clear")
  
